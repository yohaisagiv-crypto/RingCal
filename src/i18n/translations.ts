const _langCache: Record<string, typeof t.he> = {}

export function getLang(lang: string): typeof t.he {
  if (_langCache[lang]) return _langCache[lang]
  const specific = (t as unknown as Record<string, typeof t.he>)[lang]
  const merged = { ...(t.en as unknown as typeof t.he), ...(specific ?? {}) }
  _langCache[lang] = merged
  return merged
}
